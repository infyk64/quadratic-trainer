import { useState } from "react";

type Tab = "student" | "teacher" | "admin";

export function UserGuide() {
  const [activeTab, setActiveTab] = useState<Tab>("student");
  const role = localStorage.getItem("role") || "student";

  const tabs: { key: Tab; label: string; visible: boolean }[] = [
    { key: "student", label: "Для студента", visible: true },
    { key: "teacher", label: "Для преподавателя", visible: role === "teacher" || role === "admin" },
    { key: "admin", label: "Для администратора", visible: role === "admin" },
  ];

  return (
    <div className="page-container">
      <h1>Справочник — Руководство пользователя</h1>

      {/* Табы */}
      <div style={{ display: "flex", gap: "8px", marginBottom: "24px" }}>
        {tabs
          .filter((t) => t.visible)
          .map((tab) => (
            <button
              key={tab.key}
              onClick={() => setActiveTab(tab.key)}
              style={{
                padding: "10px 20px",
                borderRadius: "8px",
                border: activeTab === tab.key ? "2px solid var(--accent)" : "1px solid var(--border)",
                background: activeTab === tab.key ? "var(--accent2)" : "var(--surface)",
                color: "white",
                cursor: "pointer",
                fontSize: "15px",
                fontWeight: activeTab === tab.key ? 600 : 400,
                transition: "all 0.2s",
              }}
            >
              {tab.label}
            </button>
          ))}
      </div>

      {/* Контент */}
      {activeTab === "student" && <StudentGuide />}
      {activeTab === "teacher" && <TeacherGuide />}
      {activeTab === "admin" && <AdminGuide />}
    </div>
  );
}

