// ===== Компонент секции =====
function GuideSection({ title, content }: { title: string; content: string[] }) {
  return (
    <div className="section-card">
      <h2>{title}</h2>
      <div style={{ display: "flex", flexDirection: "column", gap: "8px", marginTop: "12px" }}>
        {content.map((line, i) => (
          <p
            key={i}
            style={{
              color: line.startsWith("—") ? "var(--text2)" : "var(--text)",
              fontSize: "15px",
              lineHeight: "1.6",
              margin: 0,
              paddingLeft: line.startsWith("—") ? "16px" : 0,
            }}
          >
            {line}
          </p>
        ))}
      </div>
    </div>
  );
}